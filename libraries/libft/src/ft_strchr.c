#include "../libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (c >= 0)
		c = c - 256;
	c = c + 256;
	while ((*s != c) && (*s != '\0'))
		s++;
	if ((*s == '\0') && (c == '\0'))
		return ((char *)s);
	if (*s == '\0')
		return (NULL);
	return ((char *)s);
}
